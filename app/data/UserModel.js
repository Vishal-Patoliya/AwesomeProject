class UserModel {

    constructor() {
        stateName,
        username,
        email,
        mobile,
        gender,
        address;
    }

    _setStateName(stateName) {
        this.stateName = stateName;
    }

    _setUserName(username) {
        this.username = username;
    }

    _setEmail(email) {
        this.email = email;
    }

    _setMobile(mobile) {
        this.mobile = mobile;
    }

    _setGender(gender) {
        this.gender = gender;
    }

    _setAddress(address) {
        this.address = address;
    }
}